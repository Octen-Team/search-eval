"""Automated first-pass rubric review: a cross-family model panel votes on each flagged rubric.

Why a panel: rubrics are generated by claude-sonnet-4.6; reviewing with the same model alone
invites self-preference. The panel resolves at runtime to one model per family (openai,
google, anthropic) from OpenRouter; ≥2/3 pass votes → auto-approved. Auto-approval is
auditable and reversible: the record gets auto_review{votes, issues, panel} and
reviewed_by="auto_panel_v1" — a human can re-open any record by flipping reviewed back.

Each review re-fetches SERP grounding for the query so fact claims in the rubric are checked
against reality, not against the reviewer's memory. If grounding fetch fails, the review
proceeds without fact verification (recorded in the result).

Fail verdicts keep needs_review=true and carry the panel's concrete issues — that (much
smaller) pile is what humans actually read.

Resume: results append to --reviews-out per rubric (last-wins); relaunching skips already
reviewed qids. Rubrics file is updated in one compact pass at the end.

Usage:
  python -m src.rubric_review --queries data/main_queries.jsonl \\
      --rubrics data/rubrics.jsonl [--concurrency 4] [--limit N] [--yes]
"""
from __future__ import annotations

import argparse
import hashlib
import json
import threading
from collections import Counter
from concurrent.futures import ThreadPoolExecutor, as_completed
from pathlib import Path

import requests

from .backends import _load_dotenv
from .common import today_utc, write_jsonl_atomic
from .llm import call_llm_json

_load_dotenv()

PANEL_PREFS = {
    "openai": ["openai/gpt-5.2", "openai/gpt-5.1", "openai/gpt-5", "openai/gpt-4.1"],
    "google": ["google/gemini-3-pro", "google/gemini-2.5-pro", "google/gemini-2.5-flash"],
    "anthropic": ["anthropic/claude-sonnet-4.6", "anthropic/claude-sonnet-4.5"],
}

REVIEW_SYSTEM = """You are auditing an auto-generated evaluation rubric for a web-search quality benchmark. \
You get the search query, its metadata labels, the rubric (JSON), and fresh Google/Bing SERP grounding material.

Audit dimensions:
1. Intent: does intent_interpretation match what this query actually asks for?
2. Checklist quality: every item must be an objective statement verifiable one-by-one against a \
result list — filler like "results should be relevant" is a major issue.
3. Factual soundness: entities/claims in the rubric must be real. Verify against the grounding \
material; a load-bearing claim (e.g. a w3 checklist item) that neither the grounding nor solid \
knowledge supports is a major issue; peripheral unverifiable details are minor.
4. Grounding contamination: the checklist must describe the INTENT's requirements, not paraphrase \
whatever the SERP happens to contain. Items that merely mirror specific SERP results are a major issue.
5. Disqualifiers: must be real, plausible traps (confusable entities, common misreadings, SEO shapes) — \
padded/forced disqualifiers are a minor issue.
6. Weights: w3 must mark genuinely-required points. Misweighted items are minor issues.

Verdict rule: "fail" only when there is at least one MAJOR issue (wrong intent, filler checklist \
item, fabricated/unsupported load-bearing claim, SERP parroting). Minor issues alone → "pass".

Output JSON only:
{"verdict": "pass" | "fail", "issues": [{"field": "checklist.c2", "severity": "major" | "minor", "note": "..."}]}
An empty issues array with verdict "pass" is normal for a good rubric."""


def rubric_sha(rec: dict) -> str:
    return hashlib.sha256(json.dumps(rec["rubric"], sort_keys=True, ensure_ascii=False).encode()).hexdigest()[:12]


def should_review(rec: dict, prior: dict | None) -> bool:
    """Re-review when never reviewed, the prior attempt errored, or the rubric content changed
    since the prior review (regenerated rubrics must not be skipped by the resume store)."""
    return prior is None or prior["verdict"] == "error" or prior.get("rubric_sha") != rubric_sha(rec)


def resolve_panel(override: list[str] | None = None) -> list[str]:
    if override:
        return override
    resp = requests.get("https://openrouter.ai/api/v1/models", timeout=30)
    resp.raise_for_status()  # a 502/HTML page used to surface as a baffling JSONDecodeError
    ids = {m["id"] for m in resp.json()["data"]}
    panel = []
    for fam, cands in PANEL_PREFS.items():
        pick = next((c for c in cands if c in ids), None)
        if pick:
            panel.append(pick)
    if len(panel) < 2:
        raise RuntimeError(f"could not resolve at least 2 panel families (got {panel})")
    return panel


def _call_reviewer(model: str, user: str) -> dict:
    # retries/backoff/parse-recovery live in llm.call_llm_json
    return call_llm_json(REVIEW_SYSTEM, user, model=model, max_tokens=1024)


def review_one(q: dict, rec: dict, panel: list[str]) -> dict:
    grounded = True
    try:
        from .serp import serp_both, render_grounding
        grounding = render_grounding(serp_both(q["query"], k=6))
    except Exception as e:  # noqa: BLE001
        grounded = False
        grounding = f"(grounding unavailable: {e} — skip fact verification, audit the other dimensions)"

    user = (
        f"Query: {q['query']}\n"
        f"Labels: intent={q.get('intent')} | difficulty={q.get('difficulty')} | "
        f"freshness={q.get('freshness')} | vertical={q.get('vertical')} | "
        f"language={q.get('language')} | form={q.get('form')}\n\n"
        f"Rubric under audit:\n{json.dumps(rec['rubric'], ensure_ascii=False, indent=1)}\n\n"
        f"### SERP grounding (Google+Bing, fresh)\n{grounding}"
    )

    votes: dict[str, str] = {}
    issues: list[dict] = []
    for model in panel:
        try:
            out = _call_reviewer(model, user)
            v = str(out.get("verdict", "")).lower()
            votes[model] = v if v in ("pass", "fail") else "fail"
            for i in out.get("issues", []):
                if isinstance(i, dict):
                    issues.append({"model": model, "field": str(i.get("field", "")),
                                   "severity": str(i.get("severity", "minor")),
                                   "note": str(i.get("note", ""))[:300]})
        except Exception as e:  # noqa: BLE001
            votes[model] = "error"
            issues.append({"model": model, "field": "-", "severity": "error", "note": str(e)[:200]})

    verdict = aggregate(votes)
    return {"qid": rec["qid"], "verdict": verdict, "votes": votes, "issues": issues,
            "grounded": grounded, "panel": panel, "reviewed_at": today_utc(),
            "rubric_sha": rubric_sha(rec)}


def aggregate(votes: dict[str, str]) -> str:
    """≥2 pass votes → pass; ≥2 valid votes without a pass majority → fail; else error (stays queued)."""
    n_pass = sum(1 for v in votes.values() if v == "pass")
    n_valid = sum(1 for v in votes.values() if v in ("pass", "fail"))
    if n_pass >= 2:
        return "pass"
    if n_valid >= 2:
        return "fail"
    return "error"


def main() -> None:
    ap = argparse.ArgumentParser()
    ap.add_argument("--queries", required=True)
    ap.add_argument("--rubrics", default="data/rubrics.jsonl")
    ap.add_argument("--reviews-out", default="data/rubric_reviews.jsonl", help="append-only review log (resume store)")
    ap.add_argument("--panel", nargs="*", default=None, help="override panel model ids")
    ap.add_argument("--concurrency", type=int, default=4)
    ap.add_argument("--limit", type=int, default=None)
    ap.add_argument("--yes", action="store_true")
    args = ap.parse_args()

    queries = {q["qid"]: q for q in (json.loads(l) for l in Path(args.queries).read_text(encoding="utf-8").splitlines() if l.strip())}
    rub_path = Path(args.rubrics)
    records = {r["qid"]: r for r in (json.loads(l) for l in rub_path.read_text(encoding="utf-8").splitlines() if l.strip())}

    reviews_path = Path(args.reviews_out)
    done: dict[str, dict] = {}
    if reviews_path.exists():
        for line in reviews_path.read_text(encoding="utf-8").splitlines():
            if line.strip():
                rev = json.loads(line)
                done[rev["qid"]] = rev

    flagged = [r for r in records.values()
               if r["needs_review"] and not r["reviewed"] and r["qid"] in queries]
    todo = [r for r in flagged if should_review(r, done.get(r["qid"]))]
    if args.limit:
        todo = todo[: args.limit]

    panel = resolve_panel(args.panel)
    print(f"Flagged rubrics: {len(flagged)}; already reviewed: {len(flagged) - len(todo)}; to review: {len(todo)}", flush=True)
    print(f"Panel: {panel}", flush=True)
    print(f"Estimated: ≤{len(todo) * len(panel)} LLM calls + ≤{len(todo) * 2} SERP calls", flush=True)
    if todo and not args.yes:
        if input("Continue? [y/N] ").strip().lower() != "y":
            print("Cancelled")
            return

    lock = threading.Lock()
    n_done = 0
    with reviews_path.open("a", encoding="utf-8") as f, \
            ThreadPoolExecutor(max_workers=args.concurrency) as pool:
        futures = {pool.submit(review_one, queries[r["qid"]], r, panel): r for r in todo}
        for fut in as_completed(futures):
            rec = futures[fut]
            try:
                rev = fut.result()
            except Exception as e:  # noqa: BLE001
                rev = {"qid": rec["qid"], "verdict": "error", "votes": {}, "issues": [
                    {"model": "-", "field": "-", "severity": "error", "note": str(e)[:200]}],
                    "grounded": False, "panel": panel, "reviewed_at": today_utc()}
            with lock:
                f.write(json.dumps(rev, ensure_ascii=False) + "\n")
                f.flush()
                done[rev["qid"]] = rev
                n_done += 1
                majors = sum(1 for i in rev["issues"] if i["severity"] == "major")
                print(f"[{n_done}/{len(todo)}] {rev['qid']}: {rev['verdict'].upper()}"
                      f" ({'/'.join(rev['votes'].values()) or '-'}; {majors} major)", flush=True)

    # merge verdicts into the rubrics file (compact rewrite); counts are verdict-based over
    # the full review log, independent of whether approval was applied in an earlier run
    verdicts = Counter(rev["verdict"] for rev in done.values() if rev["qid"] in records)
    for qid, rev in done.items():
        rec = records.get(qid)
        if not rec:
            continue
        if rev.get("rubric_sha") and rev["rubric_sha"] != rubric_sha(rec):
            continue  # stale review of an older rubric version — don't apply it
        rec["auto_review"] = {k: rev[k] for k in ("verdict", "votes", "issues", "grounded", "panel", "reviewed_at")}
        if rev["verdict"] == "pass" and rec["needs_review"] and not rec.get("reviewed"):
            rec["reviewed"] = True
            rec["reviewed_by"] = "auto_panel_v1"
    # atomic: a crash mid-rewrite must not destroy the rubrics file (incl. human reviews)
    write_jsonl_atomic(rub_path, [records[qid] for qid in sorted(records)])

    print(f"\nPanel verdicts: pass={verdicts.get('pass', 0)} | fail (kept for humans, with issues)="
          f"{verdicts.get('fail', 0)} | error={verdicts.get('error', 0)}", flush=True)
    issue_fields = Counter(i["field"].split(".")[0] for r in done.values() for i in r["issues"] if i["severity"] == "major")
    if issue_fields:
        print("Major-issue fields:", dict(issue_fields.most_common()), flush=True)
    print(f"Review log → {reviews_path}; rubrics updated → {rub_path}", flush=True)


if __name__ == "__main__":
    main()
